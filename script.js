  game.winner = winner;
  game.result = {
    left: leftResult,
    right: rightResult,
  };
  game.message = `${message}${reason ? ` ${reason}` : ""} ${duel.leftPlayer} 是 ${leftResult.name}，${duel.rightPlayer} 是 ${rightResult.name}。`;
